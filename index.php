<script>
const track = document.querySelector('#hero-carousel .carousel-track');
const slides = document.querySelectorAll('#hero-carousel .carousel-slide');
const dots = document.querySelectorAll('#hero-carousel .dot');
let index = 0;

function updateCarousel(){
  track.style.transform = `translateX(-${index * 100}%)`;
  dots.forEach(dot => dot.style.background = '#888');
  dots[index].style.background = '#fff';

  // animação de entrada
  slides.forEach(slide => {
    slide.querySelectorAll('.slide-title, .slide-text, .slide-btn').forEach(el => {
      el.style.opacity = 0;

    });
  });

  const current = slides[index];
  current.querySelectorAll('.slide-title, .slide-text, .slide-btn').forEach((el, i) => {
    setTimeout(() => {
      el.style.opacity = 1;
      el.style.transform = 'translateY(0)';
    }, i * 200);
  });
}

document.getElementById('nextBtn').addEventListener('click', () => {
  index = (index + 1) % slides.length;
  updateCarousel();
});

document.getElementById('prevBtn').addEventListener('click', () => {
  index = (index - 1 + slides.length) % slides.length;
  updateCarousel();
});

dots.forEach(dot => {
  dot.addEventListener('click', () => {
    index = parseInt(dot.getAttribute('data-index'));
    updateCarousel();
  });
});

// Auto-play
setInterval(() => {
  index = (index + 1) % slides.length;
  updateCarousel();
}, 6000);

// Inicializa
updateCarousel();
</script>






