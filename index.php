<script>
document.addEventListener('DOMContentLoaded', () => {
  const track = document.querySelector('.promo-track');
  const cards = Array.from(document.querySelectorAll('.promo-col'));
  const dotsWrapper = document.querySelector('.promo-dots');

  // duplicar cards para loop infinito
  track.innerHTML += track.innerHTML;

  // modal bind
  document.querySelectorAll('.promo-col').forEach(card=>{
    card.addEventListener('click', ()=>{
      const title = card.dataset.title;
      const content = card.dataset.content;
      if(content){
        document.getElementById('promoTitle').innerHTML = title;
        document.getElementById('promoContent').innerHTML = content;
        new bootstrap.Modal(document.getElementById('promoModal')).show();
      }
    });
  });

  // criar dots
  const numSlides = cards.length;
  for(let i=0;i<numSlides;i++){
    const btn = document.createElement('button');
    if(i===0) btn.classList.add('active');
    btn.addEventListener('click', ()=> goToSlide(i));
    dotsWrapper.appendChild(btn);
  }

  let scrollPos = 0;
  let currentIndex = 0;
  let isAnimating = false;

  function getCardWidth() {
    return document.querySelector('.promo-col').offsetWidth - 80;
  }

  function updateDots() {
    dotsWrapper.querySelectorAll('button').forEach((btn,i)=>{
      btn.classList.toggle('active', i === currentIndex);
    });
  }

  function slide(offset) {
    if(isAnimating) return;
    isAnimating = true;
    scrollPos += offset;
    track.style.transition = 'transform 0.6s ease';
    track.style.transform = `translateX(${-scrollPos}px)`;
    setTimeout(()=>{
      const trackWidthHalf = track.scrollWidth / 2;
      if(scrollPos >= trackWidthHalf) scrollPos -= trackWidthHalf;
      if(scrollPos < 0) scrollPos += trackWidthHalf;
      track.style.transition = 'none';
      track.style.transform = `translateX(${-scrollPos}px)`;
      isAnimating = false;
    }, 600);
  }

  function goToSlide(index) {
    const offset = (index - currentIndex) * getCardWidth();
    currentIndex = index;
    updateDots();
    slide(offset);
  }

  document.querySelector('.carousel-btn.next').addEventListener('click', ()=>{
    currentIndex = (currentIndex+1)%numSlides;
    updateDots();
    slide(getCardWidth());
  });
  document.querySelector('.carousel-btn.prev').addEventListener('click', ()=>{
    currentIndex = (currentIndex-1+numSlides)%numSlides;
    updateDots();
    slide(-getCardWidth());
  });

  // opcional: autoplay
  setInterval(()=>{
    currentIndex = (currentIndex+1)%numSlides;
    updateDots();
    slide(getCardWidth());
  }, 6000);

  window.addEventListener('resize', ()=>{});
});
</script>




